/**
 * Single source of truth for the five chord option types: display label,
 * sensitivity (masked on the card), and icon. Consumed by both the chord form
 * (dropdown labels + row icons) and the chord card (row rendering), so the
 * icon↔type↔sensitivity mapping can never drift between the two (FR-008).
 *
 * Icons are inline Bootstrap-Icons SVG paths (person-fill, envelope, key-fill,
 * tag-fill, shield-lock-fill) — no icon dependency, matching the repo's existing
 * inline-SVG pattern. All icons are decorative (`aria-hidden`); callers provide
 * the type name as accessible text.
 */

/** Shared props for the 16×16 Bootstrap-Icons viewBox. */
function svg(path: ReactElement, size = 16): ReactElement {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      fill="currentColor"
      aria-hidden="true"
      focusable="false"
    >
      {path}
    </svg>
  );
}

